# montage integration: canonical branch uses the helper
# ---------------------------------------------------------------------------

class TestMontageCanonicalIntegration:
    def test_estimate_activity_source_uses_helper(self):
        """Regression guard: estimate_activity's canonical branch must use
        get_canonical_hrf, not read self.hbo_tree.root.right."""
        from hrfunc.hrfunc import montage
        src = inspect.getsource(montage.estimate_activity)
        assert 'get_canonical_hrf' in src
        # The old root.right fallback must be gone
        assert 'hbo_tree.root.right' not in src
        assert 'hbr_tree.root.right' not in src

    def test_localize_hrfs_source_uses_helper(self):
        """localize_hrfs should no longer inline its own glover_hrf call —
        it now goes through tree.get_canonical_hrf."""
        from hrfunc.hrfunc import montage
        src = inspect.getsource(montage.localize_hrfs)
        assert 'get_canonical_hrf' in src
        # Inline glover_hrf generation at the function level should be gone
        assert 'glover_hrf' not in src


# ---------------------------------------------------------------------------
# Round-trip: generate canonical at different sfreqs, verify distinct length
# ---------------------------------------------------------------------------

class TestCanonicalSfreqRoundTrip:
    def test_canonical_matches_scan_sfreq_not_hardcoded(self):
        """Pre-fix the canonical was locked to 7.81 Hz (t_r=0.128) regardless
        of the calling scan. Post-fix, generating at a different sfreq
        produces a kernel of a different length that matches that sfreq."""
        from hrfunc.hrtree import tree
        t = tree()
        at_781 = t.get_canonical_hrf(True, 7.81, 30.0)
        at_500 = t.get_canonical_hrf(True, 5.0, 30.0)
        at_1000 = t.get_canonical_hrf(True, 10.0, 30.0)

        # Lengths should be roughly sfreq * duration
        assert abs(len(at_781.trace) - 234) <= 10
        assert abs(len(at_500.trace) - 150) <= 10
        assert abs(len(at_1000.trace) - 300) <= 10

        # And they should be distinct objects
        assert at_781 is not at_500
        assert at_500 is not at_1000
