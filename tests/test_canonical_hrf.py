# nearest_neighbor fallback returns None on miss
# ---------------------------------------------------------------------------

class TestNearestNeighborFallback:
    def test_empty_tree_returns_none(self):
        from hrfunc.hrtree import tree
        t = tree()
        probe = _user_hrf('probe', 0.0, 0.0, 0.0)
        result, distance = t.nearest_neighbor(probe, max_distance=0.01)
        assert result is None
        assert distance == float('inf')

    def test_far_away_query_returns_none(self):
        from hrfunc.hrtree import tree
        t = tree()
        t.insert(_user_hrf('a', 0.0, 0.0, 0.0))
        far_probe = _user_hrf('probe', 100.0, 100.0, 100.0)
        result, distance = t.nearest_neighbor(far_probe, max_distance=0.01)
        assert result is None

    def test_source_no_longer_references_root_right_fallback(self):
        """Regression guard: _delete_recursive of this fallback path must
        not re-introduce a root.right reference that expects a canonical."""
        from hrfunc.hrtree import tree
        src = inspect.getsource(tree.nearest_neighbor)
        # The fallback must NOT read self.root.right as if it were canonical
        assert 'self.root.right' not in src


